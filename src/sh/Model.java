package sh;

/**
 *
 * @author Muzaffar
 */
public class Model {

    private double z;
    private double y;
    private double sigma;
    private double delta;
    private double Cdin;
    private double Fidin;
    private double B;
    private double D;
    private double F;
    private double H;
    private double mju;

    Model() {
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getSigma() {
        return sigma;
    }

    public void setSigma(double sigma) {
        this.sigma = sigma;
    }

    public double getDelta() {
        return delta;
    }

    public void setDelta(double delta) {
        this.delta = delta;
    }

    public double getCdin() {
        return Cdin;
    }

    public void setCdin(double Cdin) {
        this.Cdin = Cdin;
    }

    public double getFidin() {
        return Fidin;
    }

    public void setFidin(double Fidin) {
        this.Fidin = Fidin;
    }

    public double getB() {
        return B;
    }

    public void setB(double B) {
        this.B = B;
    }

    public double getD() {
        return D;
    }

    public void setD(double D) {
        this.D = D;
    }

    public double getF() {
        return F;
    }

    public void setF(double F) {
        this.F = F;
    }

    public double getH() {
        return H;
    }

    public void setH(double H) {
        this.H = H;
    }

    public double getMju() {
        return mju;
    }

    public void setMju(double mju) {
        this.mju = mju;
    }

}
